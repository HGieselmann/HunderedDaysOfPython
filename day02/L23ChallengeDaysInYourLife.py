print("So how many Weeks, Days, Hour do you have in your life approximately?")
years = int(input("So how old do you want to become?"))
weeks = years * 52
days = weeks * 365
hours = days * 24
minutes = hours * 60
seconds = hours * 60
print(f"You have {weeks} weeks, {days} days, {hours} hours, {minutes} minutes and {seconds} seconds in your life if you reach {years}.")